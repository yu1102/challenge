#No.1_数字を入力値として受け取り、その数字を2乗した戻り値を返す関数を書いてみよう。

def f():
    """
    入力値を2乗した値を算出する関数
    入力値：整数
    """
    try:
        num = input("type a number: ")
        in_num = int(num)
        return in_num ** 2
    except ValueError:
        print("数字を入力してください。")

print(f())



